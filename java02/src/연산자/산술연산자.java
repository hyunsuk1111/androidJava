package 연산자;

public class 산술연산자 {

	public static void main(String[] args) {
		// 산술연산자 : + - * / %
		int x = 200;
		int y = 100; 
		
		System.out.println("덧셈 : " + (x+y));
		System.out.println("뺄셈 : " + (x-y));
		System.out.println("곱셈 : " + (x*y));
		System.out.println("나눗셈(몫) : " + (x/y));
		System.out.println("나눗셈(나머지) : " + (x%y));
		
	}

}
